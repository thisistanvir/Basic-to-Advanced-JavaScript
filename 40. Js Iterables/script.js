/**
 * JS Iterables
 *
 * Iterables are iterable objects (like Arrays).
 * Iterables can be accessed with simple and efficient code.
 * Iterables can be iterated over with for..of loops
 *
 * String
 * Array
 * Set
 * Map
 */

// String
{
	const name = "W3Schools";

	for (const x of name) {
		console.log(x);
	}
}

// Array
{
	const letters = ["a", "b", "c"];

	for (const x of letters) {
		console.log(x);
	}
}
